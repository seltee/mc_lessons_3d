void drawFPNumber(int x, int y, int number)
{
  char buffer[32];
  bool isNeg = number < 0;
  if (isNeg)
  {
    number = -number;
  }

  int leftPart = FPFloor(number);
  int rightPart = FPFraction(number);

  buffer[0] = isNeg ? '-' : 0;
  char *sBuffer = isNeg ? buffer + 1 : buffer;
  itoa(leftPart, sBuffer);

  int whereIsComma = strlen(sBuffer);
  sBuffer[whereIsComma] = ',';

  if (rightPart)
  {
    for (int i = 0; i < 4; i++)
    {
      rightPart *= 10;
      int s = rightPart / ONE;
      int r = s * ONE;
      sBuffer[whereIsComma + 1 + i] = '0' + s;
      sBuffer[whereIsComma + 2 + i] = 0;
      rightPart -= r;
      if (rightPart == 0)
      {
        break;
      }
    }
  }
  else
  {
    sBuffer[whereIsComma + 1] = '0';
    sBuffer[whereIsComma + 2] = 0;
  }

  displayText(buffer, 3, x, y, false);
}

